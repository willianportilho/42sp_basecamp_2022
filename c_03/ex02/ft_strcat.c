char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	i_2;

	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	i_2 = 0;
	while (src[i_2] != '\0')
	{
		dest[i] = src[i_2];
		i_2++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
